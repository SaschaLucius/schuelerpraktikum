<h1>HTML</h1>
<p>
	HTML steht für Hypertext Markup Language und ist eine Programmiersprache, die verwendet wird, um
	Webseiten zu erstellen. HTML dient dazu, den Inhalt einer Webseite zu strukturieren und zu
	organisieren.
</p>
<p>
	Stell dir vor, du möchtest ein Haus bauen. HTML ist wie das Gerüst oder das Grundgerüst des
	Hauses. Es hilft dabei, die verschiedenen Teile der Webseite zu definieren und anzuordnen, ähnlich
	wie Wände, Türen und Fenster in einem Haus.
</p>

<p>
	Mit HTML kannst du verschiedene Elemente erstellen, wie Überschriften, Absätze, Bilder, Links und
	Listen. Diese Elemente helfen dabei, den Text und die Medien auf einer Webseite zu organisieren
	und zu formatieren.
</p>
