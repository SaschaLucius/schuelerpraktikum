<h1>Svelte</h1>

<p>
	Svelte ist ein Werkzeug, das verwendet wird, um Webseiten und Webanwendungen zu erstellen. Es
	hilft dabei, den Code zu organisieren und die Struktur der Webseite zu definieren.
</p>
<p>
	Svelte ist wie die verschiedenen Räume und Funktionen in einem Haus. Es ermöglicht dir, diese
	Räume zu erstellen und miteinander zu verbinden, um ein funktionierendes Haus zu erstellen.
</p>

<p>
	Zum Beispiel kannst du mit Svelte Überschriften, Buttons und Formulare erstellen und sie
	miteinander verbinden, um eine interaktive Webseite zu erstellen. Svelte hilft dir dabei, deine
	Webseite übersichtlich und gut strukturiert zu halten, damit du leichter daran arbeiten und
	Änderungen vornehmen kannst.
</p>
